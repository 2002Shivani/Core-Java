package com.basic.operator;

public class Swapping {

	
	public static void main(String[] args) {
	int a = 10;
	int b = 20;
	
	int temp = a;
	a=20;
	b=temp;
	
	System.out.println("a="+a+" b="+b);
	
	int c =10;
	int d =20;
	
	c = c+d;
	d = c-d;
	c = c-d;
	
	System.out.println("c="+c+" d="+d);
	
	
	
	
	}
}
